package com.flynava.jupiter.model;

public class AnalystDistributorPerc {
	private String distributor;
	private float distributorValue;
	private float distributorperc;
	private float otherperc;

	public String getDistributor() {
		return distributor;
	}

	public void setDistributor(String distributor) {
		this.distributor = distributor;
	}

	public float getDistributorValue() {
		return distributorValue;
	}

	public void setDistributorValue(float distributorValue) {
		this.distributorValue = distributorValue;
	}

	public float getDistributorperc() {
		return distributorperc;
	}

	public void setDistributorperc(float distributorperc) {
		this.distributorperc = distributorperc;
	}

	public float getOtherperc() {
		return otherperc;
	}

	public void setOtherperc(float otherperc) {
		this.otherperc = otherperc;
	}

}
