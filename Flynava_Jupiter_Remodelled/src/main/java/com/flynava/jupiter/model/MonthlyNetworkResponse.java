package com.flynava.jupiter.model;

public class MonthlyNetworkResponse {

	private String year;
	private String month;
	private String region;
	private String country;
	private String pos;
	private String compartment;
	
	private String monthVLYR;
	
	private String combinationKey;
	
	
	public String getYear() {
		return year;
	}
	public void setYear(String year) {
		this.year = year;
	}
	public String getRegion() {
		return region;
	}
	public void setRegion(String region) {
		this.region = region;
	}
	public String getCountry() {
		return country;
	}
	public void setCountry(String country) {
		this.country = country;
	}
	public String getPos() {
		return pos;
	}
	public void setPos(String pos) {
		this.pos = pos;
	}
	public String getCompartment() {
		return compartment;
	}
	public void setCompartment(String compartment) {
		this.compartment = compartment;
	}
	public String getMonthVLYR() {
		return monthVLYR;
	}
	public void setMonthVLYR(String monthVLYR) {
		this.monthVLYR = monthVLYR;
	}
	public String getMonth() {
		return month;
	}
	public void setMonth(String month) {
		this.month = month;
	}
	public String getCombinationKey() {
		return combinationKey;
	}
	public void setCombinationKey(String combinationKey) {
		this.combinationKey = combinationKey;
	}
	
	
	

}
