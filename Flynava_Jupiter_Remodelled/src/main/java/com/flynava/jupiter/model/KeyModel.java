package com.flynava.jupiter.model;

public class KeyModel {
	
	private String key;
	private String subKey;
	public String getKey() {
		return key;
	}
	public void setKey(String key) {
		this.key = key;
	}
	public String getSubKey() {
		return subKey;
	}
	public void setSubKey(String subKey) {
		this.subKey = subKey;
	}
	
}
