package com.flynava.jupiter.model;

public class AnalystChannelPerc {

	private String channel;
	private float channelValue;
	private float channelPerc;

	public String getChannel() {
		return channel;
	}

	public void setChannel(String channel) {
		this.channel = channel;
	}

	public float getChannelValue() {
		return channelValue;
	}

	public void setChannelValue(float channelValue) {
		this.channelValue = channelValue;
	}

	public float getChannelPerc() {
		return channelPerc;
	}

	public void setChannelPerc(float channelPerc) {
		this.channelPerc = channelPerc;
	}
}
