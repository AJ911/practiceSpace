package com.flynava.jupiter.model;

public class TriggerType {

	private String trgrEvent;
	private String desc;
	private String trgrEventType;
	private String dshbrd;
	private String active;
	private String trgrType;

	public String getDesc() {
		return desc;
	}

	public void setDesc(String desc) {
		this.desc = desc;
	}

	public String getTrgrEvent() {
		return trgrEvent;
	}

	public void setTrgrEvent(String trgrEvent) {
		this.trgrEvent = trgrEvent;
	}

	public String getTrgrEventType() {
		return trgrEventType;
	}

	public void setTrgrEventType(String trgrEventType) {
		this.trgrEventType = trgrEventType;
	}

	public String getDshbrd() {
		return dshbrd;
	}

	public void setDshbrd(String dshbrd) {
		this.dshbrd = dshbrd;
	}

	public String getActive() {
		return active;
	}

	public void setActive(String active) {
		this.active = active;
	}

	public String getTrgrType() {
		return trgrType;
	}

	public void setTrgrType(String trgrType) {
		this.trgrType = trgrType;
	}

}
