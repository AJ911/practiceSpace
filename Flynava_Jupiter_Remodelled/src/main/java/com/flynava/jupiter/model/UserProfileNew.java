package com.flynava.jupiter.model;

import java.util.List;

public class UserProfileNew {
	
	private String user;
	private String name;
	private String level;
	private List<String> regionList;
	private List<String> countryList;
	private List<String> posList;
	private List<String> odList;
	private List<String> compartmentList;
	public String getUser() {
		return user;
	}
	public void setUser(String user) {
		this.user = user;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getLevel() {
		return level;
	}
	public void setLevel(String level) {
		this.level = level;
	}
	public List<String> getRegionList() {
		return regionList;
	}
	public void setRegionList(List<String> regionList) {
		this.regionList = regionList;
	}
	public List<String> getCountryList() {
		return countryList;
	}
	public void setCountryList(List<String> countryList) {
		this.countryList = countryList;
	}
	public List<String> getPosList() {
		return posList;
	}
	public void setPosList(List<String> posList) {
		this.posList = posList;
	}
	public List<String> getOdList() {
		return odList;
	}
	public void setOdList(List<String> odList) {
		this.odList = odList;
	}
	public List<String> getCompartmentList() {
		return compartmentList;
	}
	public void setCompartmentList(List<String> compartmentList) {
		this.compartmentList = compartmentList;
	}
	
	

}
